  memo TEXT NOT NULL DEFAULT '',
  amount INTEGER NOT NULL DEFAULT 0,
  category_id BIGINT NOT NULL DEFAULT 0,
  subcategory_id BIGINT NOT NULL DEFAULT 0,
  wallet_id BIGINT NOT NULL DEFAULT 0,
