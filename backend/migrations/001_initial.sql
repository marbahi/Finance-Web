);

CREATE TABLE IF NOT EXISTS budget (
  id BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL DEFAULT '',
  amount INTEGER NOT NULL DEFAULT 0,
  spent INTEGER NOT NULL DEFAULT 0,
  status INTEGER NOT NULL DEFAULT 0,
