CREATE TABLE IF NOT EXISTS account (
  id BIGSERIAL PRIMARY KEY,
  type INTEGER NOT NULL DEFAULT 0,
  name TEXT NOT NULL DEFAULT '',
  currency TEXT NOT NULL DEFAULT 'IDR',
  ordering INTEGER NOT NULL DEFAULT 0,
  currency_symbol TEXT NOT NULL DEFAULT 'Rp',
  balance INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS wallet (
  id BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL DEFAULT '',
  color TEXT NOT NULL DEFAULT '#2563eb',
  amount INTEGER NOT NULL DEFAULT 0,
  initial_amount INTEGER NOT NULL DEFAULT 0,
  active INTEGER NOT NULL DEFAULT 1,
  account_id BIGINT NOT NULL DEFAULT 1,
  ordering INTEGER NOT NULL DEFAULT 0,
  exclude INTEGER NOT NULL DEFAULT 0,
  currency TEXT NOT NULL DEFAULT 'IDR',
  icon INTEGER NOT NULL DEFAULT 0,
  type INTEGER NOT NULL DEFAULT 0,
  due_date BIGINT NOT NULL DEFAULT 0,
  statement_date BIGINT NOT NULL DEFAULT 0,
  credit_limit INTEGER NOT NULL DEFAULT 0,
  hidden INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS category (
  id BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL DEFAULT '',
  color TEXT NOT NULL DEFAULT '#78716c',
  type INTEGER NOT NULL DEFAULT 0,
  active INTEGER NOT NULL DEFAULT 1,
  ordering INTEGER NOT NULL DEFAULT 0,
  icon INTEGER NOT NULL DEFAULT 0,
  account_id BIGINT NOT NULL DEFAULT 1,
